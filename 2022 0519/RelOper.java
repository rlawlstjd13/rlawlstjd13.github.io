//관계 연산자
public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // 같은가?
		System.out.println(55 != 55); // 다른가?
		System.out.println(55 > 55); // x가 y보다 큰가?
		System.out.println(55 < 55); // x가 y보다 작은가?
		System.out.println(55 >= 55);// 크거나 같은가?
		System.out.println(55 <= 55);// 작거나 같은가?
	}
}